"""
Performance benchmark script: tests PySpark at different record counts.
Uses the same SERP data (Parquet) generated by the DuckDB benchmark.

Usage (from project root):
    spark-submit pyspark/benchmark.py
    spark-submit pyspark/benchmark.py --data data/serp_data.parquet
    spark-submit pyspark/benchmark.py --counts 1000000 10000000 50000000
"""

import argparse
import time
import sys
import os
import json
from datetime import datetime

from pyspark.sql import SparkSession
from pyspark.sql import functions as F
from pyspark.sql.window import Window


def create_spark_session():
    """Create a SparkSession (uses Spark Connect if SPARK_REMOTE is set)."""
    builder = SparkSession.builder.appName("SERP Benchmark - PySpark")
    # Only set driver memory when not using Spark Connect (server-side config)
    if not os.environ.get("SPARK_REMOTE"):
        builder = builder.config("spark.driver.memory", "8g")
    builder = builder.config("spark.sql.shuffle.partitions", "8")
    return builder.getOrCreate()


def percentile_rank_by_domain(df, max_id=None):
    """Calculate percentile ranks for domains based on average position."""
    start = time.time()

    filtered = df
    if max_id is not None:
        filtered = df.filter(F.col("id") <= max_id)

    result = (
        filtered
        .filter(F.col("domain").isNotNull() & (F.col("domain") != ""))
        .groupBy("domain")
        .agg(
            F.count("*").alias("result_count"),
            F.avg("rank").alias("avg_rank"),
            F.percentile_approx("rank", 0.5).alias("median_rank"),
            F.percentile_approx("rank", 0.25).alias("p25_rank"),
            F.percentile_approx("rank", 0.75).alias("p75_rank"),
            F.percentile_approx("rank", 0.95).alias("p95_rank"),
        )
        .orderBy("avg_rank")
        .limit(100)
    )

    # Force execution
    count = result.count()
    elapsed = time.time() - start

    return {"query": "percentile_rank_by_domain", "elapsed_seconds": elapsed, "rows_returned": count}


def rank_deltas(df, max_id=None):
    """Calculate rank deltas (change in position over time) for URLs."""
    start = time.time()

    filtered = df
    if max_id is not None:
        filtered = df.filter(F.col("id") <= max_id)

    window_spec = Window.partitionBy("url", "query").orderBy("timestamp")

    result = (
        filtered
        .withColumn("previous_rank", F.lag("rank").over(window_spec))
        .filter(F.col("previous_rank").isNotNull())
        .withColumn("rank_delta", F.col("rank") - F.col("previous_rank"))
        .orderBy(F.abs(F.col("rank_delta")).desc())
        .limit(100)
        .select("url", "query", "rank", "previous_rank", "rank_delta", "timestamp")
    )

    count = result.count()
    elapsed = time.time() - start

    return {"query": "rank_deltas", "elapsed_seconds": elapsed, "rows_returned": count}


def top_domains_by_aggregation(df, max_id=None):
    """Aggregate statistics by domain: count, avg rank, min/max."""
    start = time.time()

    filtered = df
    if max_id is not None:
        filtered = df.filter(F.col("id") <= max_id)

    result = (
        filtered
        .filter(F.col("domain").isNotNull() & (F.col("domain") != ""))
        .groupBy("domain")
        .agg(
            F.count("*").alias("total_results"),
            F.countDistinct("query").alias("unique_queries"),
            F.avg("rank").alias("avg_rank"),
            F.min("rank").alias("best_rank"),
            F.max("rank").alias("worst_rank"),
            F.countDistinct("url").alias("unique_urls"),
        )
        .filter(F.col("total_results") > 10)
        .orderBy(F.col("total_results").desc())
        .limit(50)
    )

    count = result.count()
    elapsed = time.time() - start

    return {"query": "top_domains_by_aggregation", "elapsed_seconds": elapsed, "rows_returned": count}


def run_benchmark(spark, data_path, test_counts=None):
    """Run performance benchmarks at different record counts."""
    if test_counts is None:
        test_counts = [
            1000, 5000, 10000, 20000, 50000,
            100000, 200000, 500000,
            1000000, 2000000, 5000000,
            10000000, 15000000, 20000000,
            25000000, 30000000, 40000000, 50000000, 100000000,
        ]

    print(f"Loading data from {data_path}...")
    load_start = time.time()
    full_df = spark.read.parquet(data_path)
    full_df.cache()
    total_rows = full_df.count()
    load_time = time.time() - load_start
    print(f"Loaded {total_rows:,} rows in {load_time:.2f}s")

    results = []

    for target_count in test_counts:
        if target_count > total_rows:
            print(f"\nSkipping {target_count:,} records (only {total_rows:,} available)")
            continue

        print(f"\n{'=' * 60}")
        print(f"Benchmarking at {target_count:,} records")
        print(f"{'=' * 60}")

        # IDs are sequential 1..N, so max_id == target_count
        max_id = target_count

        try:
            actual_count = full_df.filter(F.col("id") <= max_id).count()
            print(f"Running analytical queries (filtering to {target_count:,} records, "
                  f"max_id={max_id}, actual filtered count={actual_count:,})...")

            p_result = percentile_rank_by_domain(full_df, max_id=max_id)
            d_result = rank_deltas(full_df, max_id=max_id)
            a_result = top_domains_by_aggregation(full_df, max_id=max_id)

            result = {
                "record_count": target_count,
                "percentile_seconds": p_result["elapsed_seconds"],
                "delta_seconds": d_result["elapsed_seconds"],
                "aggregation_seconds": a_result["elapsed_seconds"],
                "memory_delta_mb": 0.0,
                "memory_after_mb": 0.0,
                "disk_read_mb": 0.0,
                "disk_write_mb": 0.0,
                "status": "success",
            }
            results.append(result)

            print(f"\n[OK] Results at {target_count:,} records:")
            print(f"  Percentile query: {p_result['elapsed_seconds']:.3f}s")
            print(f"  Delta query: {d_result['elapsed_seconds']:.3f}s")
            print(f"  Aggregation query: {a_result['elapsed_seconds']:.3f}s")

        except Exception as e:
            print(f"\n[ERROR] at {target_count:,} records: {e}")
            import traceback
            traceback.print_exc()
            results.append({"record_count": target_count, "status": "error", "error": str(e)})

    return results


def print_summary(results):
    print(f"\n{'=' * 120}")
    print("BENCHMARK SUMMARY (PySpark)")
    print(f"{'=' * 120}")
    print(f"{'Records':<15} {'Percentile':<12} {'Delta':<12} {'Aggregation':<12} {'Status':<10}")
    print("-" * 120)

    for r in results:
        if r.get("status") == "success":
            print(f"{r['record_count']:>13,}  "
                  f"{r['percentile_seconds']:>10.3f}s  "
                  f"{r['delta_seconds']:>10.3f}s  "
                  f"{r['aggregation_seconds']:>10.3f}s  "
                  f"{'OK':<10}")
        else:
            status = r.get("status", "unknown")
            error = r.get("error", "")[:30]
            print(f"{r['record_count']:>13,}  "
                  f"{'N/A':<12}  {'N/A':<12}  {'N/A':<12}  "
                  f"{status.upper() + ': ' + error:<10}")

    print(f"{'=' * 120}")


def save_results(results, output_path):
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, "w") as f:
        json.dump({"timestamp": datetime.now().isoformat(), "results": results}, f, indent=2)
    print(f"\nResults saved to: {output_path}")


